$(window).scroll(function () {
    var scroll = $(this).scrollTop();

    if (scroll > 130) {
        $('.menuuuu').addClass('lalala');
    } else {
        $('.menuuuu').removeClass('lalala');
    }
});

$(window).scroll(function () {
  var scroll = $(this).scrollTop();

  if (scroll > 1000) {
      $('.up_arrow').fadeIn();
      $('.up_arrow').css("right","30px");
  } else {
      $('.up_arrow').fadeOut();
      $('.up_arrow').css("right","0px");
  }
});













